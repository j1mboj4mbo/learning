age = 3
if age <= 2:
    print(f"Osoba jest niemowlęciem.")
if age > 2 and age <= 4:
    print(f"Osoba jest dzieckiem, które uczy się chodzić.")
if age > 4 and age <= 13:
    print(f"Osoba jest dzieckiem.")
if age > 13 and age <= 20:
    print(f"Osoba jest nastolatkiem.")
if age > 20 and age <= 65:
    print(f"Osoba jest dorosłym.")
if age > 65:
    print(f"Osoba jest seniorem.")